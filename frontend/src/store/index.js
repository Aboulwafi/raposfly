import Vue from 'vue'
import Vuex from 'vuex'

// ------------------------------------------------------------------------- //
// CONFIGURATION                                                             //
// ------------------------------------------------------------------------- //

// Use vuex
Vue.use(Vuex)

// ------------------------------------------------------------------------- //
// EXPORT                                                                    //
// ------------------------------------------------------------------------- //

export default new Vuex.Store({
    state: {
        count: 0
    },
    mutations: {
        increment (state) {
            state.count++
        }
    }
})
